    private void DIDeleteItemButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DIDeleteItemButtonActionPerformed
        // TODO add your handling code here:
        
        String ic = (String)DICategoryComboBox.getSelectedItem();
        String in = (String)DIItemNameComboBox.getSelectedItem();
               try
        {
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/pcc","root","");
            Statement st = conn.createStatement(); 
            String query1 = "DELETE FROM item WHERE ITEM_NAME = '"+in+"' AND ITEM_CATEGORY = '"+ic+"'";
            String query2 = "DELETE FROM inventory WHERE ITEM_NAME = '"+in+"'";
            int i = st.executeUpdate(query1);
            int j = st.executeUpdate(query2);
            if(i == 1 && j == 1)
            {
                JOptionPane optionPane = new JOptionPane("Data Deleted",JOptionPane.OK_OPTION);
                JDialog dialog = optionPane.createDialog("Success!");
                dialog.setAlwaysOnTop(true); // to show top of all other application
                dialog.setVisible(true); // to visible the dialog
            }
         
        }catch(SQLException ex)
        {
                Logger.getLogger(AdminDashBoard.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_DIDeleteItemButtonActionPerformed

