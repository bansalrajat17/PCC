    public void fillDICategoryComboBox()
    {
        DICategoryComboBox.removeAllItems();
        DIItemNameComboBox.removeAllItems();
            try
        {
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/pcc","root","");
            Statement st = conn.createStatement(); 
            String query = "SELECT DISTINCT ITEM_CATEGORY FROM item";
            ResultSet set = st.executeQuery(query);
            while(set.next())
            {
                System.out.println(set.getString("ITEM_CATEGORY"));
                DICategoryComboBox.addItem(set.getString("ITEM_CATEGORY"));                
            }
        }catch(SQLException ex)
        {
                Logger.getLogger(AdminDashBoard.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    public void fillDIItemComboBox()
    {
            
            String si = (String)DICategoryComboBox.getSelectedItem();
            try
        {
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/pcc","root","");
            Statement st = conn.createStatement(); 
            String query = "SELECT ITEM_NAME FROM item WHERE ITEM_CATEGORY = '"+si+"'";
            ResultSet set = st.executeQuery(query);
            while(set.next())
            {
                System.out.println(set.getString("ITEM_NAME"));
                DIItemNameComboBox.addItem(set.getString("ITEM_NAME"));                
            }
        }catch(SQLException ex)
        {
                Logger.getLogger(AdminDashBoard.class.getName()).log(Level.SEVERE, null, ex);
        } 
    }
